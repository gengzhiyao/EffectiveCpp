#include <iostream>

/**
 * !为多态基类声明virtual析构函数
 * 注意标题：**多态基类**
 * 意味着：当基类中含有虚函数时，一般才会声明基类的析构函数为虚函数
 * 另外，本条款以一个工程函数为例子，强调了工厂函数一般会返回基类的指针，如果基类中不声明虚析构函数，那么会造成内存泄露，更一般地说，是造成资源泄露
 * 但又不能一般地将所有的基类都声明为虚析构函数，因为那会导致派生类对象的膨胀，因此还可能造成对象的不可移植特性，因为C中并没有虚函数机制，C调用接口的时候我们需要将指针进行调整，但不同的编译器对C++对象模型的实现不一致
 * 不要尝试继承标准库中的类，除非标准明确哪种类可以继承
 * 在现代C++中，我们可以使用final关键字拒绝其他类作为该类的子类
 */

class Base final
{
    //该类无法被继承
    virtual void CanNotOverride () final;   //final还表明不能够被其他子类进行重写
};

